public class whilevsdowhileloop {
    public static void main(String[] args) {
        int x = 6;
        while (x <= 5){
            System.out.println(x+" Bangladesh");
            x++;
        }
        System.out.println("While vs do-while");
        int a = 6;
        do {
            System.out.println(a+ " Bangladesh");
            a++;
        }while (a <= 5);
    }
}
